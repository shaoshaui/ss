// 位运算


// 15 * 4 + 15 * 8 + 15 * 1
// 等价于位运算

// 15 << 2 + 15 << 3 + 15 << 0


// 左移
// x << y 等价于 x * 2^y 

// 右移
// x >> y 等价于 x / 2^y


// 左移和右移可取整 
// 原理是位运算操作的是整数，忽略小数部分，等同于数值的整数部
1.111 << 0 // 1
2.344 << 0 // 2
1.111 >> 0 // 1
2.344 >> 0 // 2

// 判断角色
// 00000000 & 00000010 = 00000000 = 0 = false
// 00000001 & 00000010 = 00000000 = 0 = false
// 00000010 & 00000010 = 00000010 = 2 = true
